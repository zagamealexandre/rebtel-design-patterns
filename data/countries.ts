// Priority countries for verification flow (US, UK, CA, IN, FR, ES, IT, AU)
const verificationPriorityCodes = ['US', 'GB', 'CA', 'IN', 'FR', 'ES', 'IT', 'AU'];

// Get countries ordered for verification: priority countries first, then alphabetical
export function getVerificationCountries(): Country[] {
  const priorityCountries = verificationPriorityCodes
    .map((code) => countries.find((c) => c.code === code))
    .filter((c): c is Country => c !== undefined);
  
  const remainingCountries = countries
    .filter((c) => !verificationPriorityCodes.includes(c.code))
    .sort((a, b) => a.name.localeCompare(b.name));
  
  return [...priorityCountries, ...remainingCountries];
}

